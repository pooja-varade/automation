package starpattern;

public class starpattern1 {

	public static void main(String[] args) {
		int i;
		int j;
	for(i=1;i<=5;i++)
	{
		  for(j=1;j<=5;j++)
		     {
			       if(j<=i) {
			    	   
				          System.out.print(j+"");
			              }
			     else
			{
				
				System.out.print(" ");
			}

		}
	System.out.println();
		
	}

	}

}
